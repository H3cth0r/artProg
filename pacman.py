def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index%20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index%20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:

        # in case user can still move forward.
        if valid(point + course):
            # Making the ghosts move fastar by 2
            point.move(course*2)
        else:
            options = [
                    vector(5, 0),
                    vector(-5, 0),
                    vector(0, 5),
                    vector(0, -5),
            ]
            plan = choice(options)

            # Foor loop for decition making for making ghosts smarter.
            # Gohsts will move depending on the route that is posible
            # and it make it get closer to pacman.
            min_num = 1000
            min_num_index = 10
            for i in range(len(options)):
                if valid(point + options[i]):

                    # Calculates ditance in case next posible movement.
                    dist = math.sqrt(((point.x+options[i].x) 
                           - pacman.x)**2 
                           + ((point.y+options[i].y) 
                           - pacman.y)**2)

                    # Checks wether distance is shorte than another
                    # previoud route.
                    if dist < min_num:
                        min_num = dist
                        min_num_index = i
            plan = options[min_num_index]
            course.x = plan.x
            course.y = plan.y

       
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')
        
         
    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


